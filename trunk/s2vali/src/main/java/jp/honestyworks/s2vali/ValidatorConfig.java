package jp.honestyworks.s2vali;

public class ValidatorConfig {
	
	private String validationRules;
	private String resourceBundleProperties;
	
	public String getValidationRules() {
		return validationRules;
	}
	public void setValidationRules(String validationRules) {
		this.validationRules = validationRules;
	}
	public String getResourceBundleProperties() {
		return resourceBundleProperties;
	}
	public void setResourceBundleProperties(String resourceBundleProperties) {
		this.resourceBundleProperties = resourceBundleProperties;
	}

}
